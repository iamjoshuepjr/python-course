#                     Iterating through a list 
#  We can use the for loop to iterate over the elements of a list

print('\n\n\t\t\t\t\t+--------------+')
print('\t\t\t\t\t- CREATE A SET -')
print('\t\t\t\t\t+--------------+\n')

front_end = {'Html', 'Css', 'JavaScript', 'Bootstrap', 'Nextjs', 'React'}

for technology in front_end:
    print(technology, end=' - ')
print('\n\n')
