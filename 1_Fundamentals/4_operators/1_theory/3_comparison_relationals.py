# ====================================================
#         Comparison and relationals Operators
# This operators return booleans values


number1 = 24
number2 = 16

# ====================================================
#         Equality and Inequality Operators


print()
# Equality Operator (==)
result = (number2 == number1)
print(f'Result Equality Operator (==): {result}')

print()
# Inequality Operator (!=)
result = (number2 != number1)
print(f'Result Inequality Operator (!=): {result}')

# ====================================================
#              Relationals Operators

print()
# Greater than Operator (>)
result = (number2 > number1)
print(f'Result Greater than Operator (>): {result}')

print()
# Less than Operator (<)
result = (number2 < number1)
print(f'Result Less than Operator (<): {result}')

print()
# Greater than or Equal to Operator (>=)
result = (number2 >= number1)
print(f'Result Greater than or Equal to Operator (>=): {result}')

print()
# Less than or Equal to Operator (<=)
result = (number2 <= number1)
print(f'Result Less than or Equal to Operator (<=): {result}')
print()