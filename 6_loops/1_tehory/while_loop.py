#  A While Loop statement in Python repeatedly
#  executes a target statements as long as given 
#  condition is true

counter = 1
while (counter <= 7):
    print(counter)
    counter += 1
else:
    print("End While Loop")

z, x = 7, 1
while z > 0:
    print(' ' * z + '*' * x + ' ' * z)
    x += 2
    z -= 1


