#                    Pass Statement
# The pass statement is a null statement which can be
# used as a placeholder for future code 


# Suppose we have a loop or a function that is no implemented yet, 
# but we want to implement it in the future. 
# In such cases, we can use the pass statement

# Syntax: pass

print()
print('+--------------------------+')
print('- Using the pass statement -')
print('+--------------------------+')
print()
print('* Using pass with conditional statements')

n = 10
if (n > 10):
    pass
print('Hello\n')
